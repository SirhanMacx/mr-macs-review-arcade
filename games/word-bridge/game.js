  // -- Animation tick --------------------------------------------------------
  function update(dt) {
    if (!state) return;
    state.time += dt;
    if (state.currentRound) {
      var r = state.currentRound;
      if (!r.result && phase === "playing") {
        r.timeRemaining -= dt;
        if (r.timeRemaining <= 5 && !r.lowTimePinged) {
          r.lowTimePinged = true;
          sfx.time_low();
        }
        if (r.timeRemaining <= 0) {
          r.timeRemaining = 0;
          timeoutRound();
        }
      }
      if (r.result && r.feedbackT > 0) {
        r.feedbackT -= dt;
        if (r.feedbackT <= 0) {
          r.feedbackT = 0;
          if (r.result === "cleared" && r.scholar && r.hintsUsed === 0 && phase === "playing") {
            openScholarQuestion();
          } else {
            advanceToNextRound();
          }
        }
      }
    }
    if (state.scholarResolveT > 0) {
      state.scholarResolveT -= dt;
      if (state.scholarResolveT <= 0) advanceToNextRound();
    }
    for (var p = state.particles.length - 1; p >= 0; p--) {
      var pa = state.particles[p];
      pa.life -= dt;
      pa.x += pa.vx * dt;
      pa.y += pa.vy * dt;
      pa.vy += pa.gravity * dt;
      if (pa.life <= 0) state.particles.splice(p, 1);
    }
    if (state.shake.life > 0) {
      state.shake.life = Math.max(0, state.shake.life - dt);
    }
    if (phase === "playing" && state.time - lastSnapshotTs > 10) {
      lastSnapshotTs = state.time;
      saveSnapshot();
    }
  }
  function advanceToNextRound() {
    if (state.lives <= 0) {
      gameOver();
      return;
    }
    state.round += 1;
    startRound();
  }
  function gameOver() {
    phase = "ended";
    if (dom.roundControls) dom.roundControls.classList.remove("show");
    sfx.gameOver();
    state.endReason = "Out of lives";
    finalizeRun();
    showEndScreen();
    stopMusic();
    clearSnapshot();
  }
  function finalizeRun() {
    if (state.score > state.best) {
      state.best = state.score;
      writeBest(state.best);
    }
    submitLeaderboard();
  }

  // -- Render frame ----------------------------------------------------------
  function frame(ts) {
    if (!lastFrame) lastFrame = ts;
    var dt = Math.min(0.1, (ts - lastFrame) / 1000);
    lastFrame = ts;
    if (phase === "playing") update(dt);
    drawBackground();
    if (state && state.currentRound) {
      drawTimerBar();
      drawHistoryPills();
      drawTopAndBottom();
      drawCurrentChain();
      drawKeyboard();
      drawParticles();
    }
    updateHud();
    rafHandle = requestAnimationFrame(frame);
  }

  // -- Input -----------------------------------------------------------------
  function bindInput() {
    document.addEventListener("keydown", function (e) {
      var k = e.key;
      if (phase === "playing") {
        if (e.repeat && (k.length !== 1 || !/[a-zA-Z]/.test(k))) return;
        if (k.length === 1 && /[a-zA-Z]/.test(k)) {
          if (typeLetter(k)) e.preventDefault();
          return;
        }
        if (k === "Backspace" || k === "Delete") {
          backspaceCurrent();
          e.preventDefault();
          return;
        }
        if (k === "Enter") {
          submitWord();
          e.preventDefault();
          return;
        }
        if (k === "ArrowLeft") {
          var r = state.currentRound;
          if (r) {
            r.activeSlot = Math.max(0, r.activeSlot - 1);
            sfx.letter_change();
          }
          e.preventDefault();
          return;
        }
        if (k === "ArrowRight") {
          var r2 = state.currentRound;
          if (r2) {
            r2.activeSlot = Math.min(r2.current.length - 1, r2.activeSlot + 1);
            sfx.letter_change();
          }
          e.preventDefault();
          return;
        }
        if (k === "1") { usePowerupByKey("1"); e.preventDefault(); return; }
        if (k === "2") { usePowerupByKey("2"); e.preventDefault(); return; }
        if (k === "3") { usePowerupByKey("3"); e.preventDefault(); return; }
        if (k === "4") { usePowerupByKey("4"); e.preventDefault(); return; }
        if (k === "5") { usePowerupByKey("5"); e.preventDefault(); return; }
        if (k === "p" || k === "P") { if (!e.repeat) togglePause(); e.preventDefault(); return; }
        if (k === "h" || k === "H") { useHint(); e.preventDefault(); return; }
      }
      if (phase === "paused" && (k === "p" || k === "P" || k === " ")) {
        togglePause();
        e.preventDefault();
        return;
      }
      if (k === "Escape" || k === "Esc") {
        if (phase === "playing" || phase === "paused") togglePause();
        else if (phase === "question") skipScholar();
        e.preventDefault();
      }
    });
    bindCanvasClick();
  }
  function typeLetter(letter) {
    var r = state.currentRound;
    if (!r || r.result) return false;
    return changeLetterAt(r.activeSlot, letter);
  }
  function backspaceCurrent() {
    var r = state.currentRound;
    if (!r || r.result) return;
    // Reset current slot back to last committed letter
    var prev = getLastCommitted();
    var slot = r.activeSlot;
    if (slot > 0 && r.current[slot] === prev.charAt(slot)) {
      slot = Math.max(0, slot - 1);
      r.activeSlot = slot;
    }
    if (r.current[slot] !== prev.charAt(slot)) {
      r.current[slot] = prev.charAt(slot);
      sfx.letter_change();
      // Check if any slot still differs
      r.currentDirty = false;
      for (var i = 0; i < r.current.length; i++) {
        if (r.current[i] !== prev.charAt(i)) { r.currentDirty = true; break; }
      }
    }
  }
  function usePowerupByKey(key) {
    for (var i = 0; i < state.powerups.length; i++) {
      var meta = POWERUP_META[state.powerups[i].type];
      if (meta && meta.key === key) {
        usePowerup(i);
        return;
      }
    }
  }
  function bindCanvasClick() {
    function handle(ev) {
      if (phase !== "playing") return;
      var r = state.currentRound;
      if (!r || r.result) return;
      var rect = canvas.getBoundingClientRect();
      var px, py;
      if (ev.touches && ev.touches.length) {
        px = ev.touches[0].clientX - rect.left;
        py = ev.touches[0].clientY - rect.top;
      } else if (ev.changedTouches && ev.changedTouches.length) {
        px = ev.changedTouches[0].clientX - rect.left;
        py = ev.changedTouches[0].clientY - rect.top;
      } else {
        px = ev.clientX - rect.left;
        py = ev.clientY - rect.top;
      }
      var lx = (px - offsetX) / scale;
      var ly = (py - offsetY) / scale;
      // Click on chain word slot? — set active slot
      var n = r.current.length;
      var geo = tileGeometry(n);
      var totalW = n * geo.w + (n - 1) * geo.gap;
      var startX = (LOGICAL_W - totalW) / 2;
      if (ly >= CHAIN_TOP && ly <= CHAIN_TOP + geo.h) {
        for (var i = 0; i < n; i++) {
          var sx = startX + i * (geo.w + geo.gap);
          if (lx >= sx && lx <= sx + geo.w) {
            r.activeSlot = i;
            sfx.letter_change();
            return;
          }
        }
      }
      // Click on keyboard?
      var rows = ["QWERTYUIOP", "ASDFGHJKL", "ZXCVBNM"];
      var keyW = 56, keyH = 36, gap = 4;
      var py2 = KEY_TOP;
      for (var rowI = 0; rowI < rows.length; rowI++) {
        var rowStr = rows[rowI];
        var rowW = rowStr.length * keyW + (rowStr.length - 1) * gap;
        var startK = (LOGICAL_W - rowW) / 2;
        if (ly >= py2 && ly <= py2 + keyH) {
          for (var ki = 0; ki < rowStr.length; ki++) {
            var kx = startK + ki * (keyW + gap);
            if (lx >= kx && lx <= kx + keyW) {
              typeLetter(rowStr.charAt(ki));
              return;
            }
          }
        }
        py2 += keyH + gap;
      }
    }
    canvas.addEventListener("click", handle);
    canvas.addEventListener("touchstart", function (e) {
      handle(e);
      if (e.cancelable) e.preventDefault();
    }, { passive: false });
  }

