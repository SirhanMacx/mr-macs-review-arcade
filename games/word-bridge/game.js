  // Final puzzle bank: 50+ each — let's verify counts and fall back if short.
  // Helper to verify shortest path with BFS using dict.
  function bfsShortestSteps(start, end, dictSet) {
    if (start === end) return 0;
    if (start.length !== end.length) return -1;
    var queue = [start];
    var dist = Object.create(null);
    dist[start] = 0;
    while (queue.length) {
      var w = queue.shift();
      if (w === end) return dist[w];
      var d = dist[w];
      if (d > 8) return -1; // cap search
      for (var i = 0; i < w.length; i++) {
        for (var c = 65; c < 91; c++) {
          var letter = String.fromCharCode(c);
          if (letter === w.charAt(i)) continue;
          var cand = w.substr(0, i) + letter + w.substr(i + 1);
          if (!dictSet[cand]) continue;
          if (dist[cand] != null) continue;
          dist[cand] = d + 1;
          if (cand === end) return d + 1;
          queue.push(cand);
        }
      }
    }
    return -1;
  }
  // Validate puzzles at runtime, drop any with no path; cache shortest.
  function validatePuzzles(arr, dictSet) {
    var out = [];
    for (var i = 0; i < arr.length; i++) {
      var p = arr[i];
      if (!p.start || !p.end || !p.theme) continue;
      if (!dictSet[p.start] || !dictSet[p.end]) continue;
      var min = bfsShortestSteps(p.start, p.end, dictSet);
      if (min < 1 || min > 8) continue;
      p.minSteps = min;
      out.push(p);
    }
    return out;
  }
