  // -- Puzzle selection ------------------------------------------------------
  function pickPuzzle(round) {
    var bank = (difficulty === 4) ? PUZZLES_4 : (difficulty === 6 ? PUZZLES_6 : PUZZLES_5);
    if (!bank.length) bank = PUZZLES_5;
    var pool = bank;
    if (state && state.recentPuzzles && state.recentPuzzles.length) {
      var fresh = pool.filter(function (p) {
        var key = p.start + ">" + p.end;
        return state.recentPuzzles.indexOf(key) < 0;
      });
      if (fresh.length > 0) pool = fresh;
    }
    return pool[Math.floor(Math.random() * pool.length)];
  }
  function isScholarPuzzle(round) {
    if (round < 2) return false;
    return Math.random() < (1 / SCHOLAR_FREQ);
  }
