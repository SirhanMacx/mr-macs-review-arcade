  function usePowerup(idxOrType) {
    if (phase !== "playing") return;
    var r = state.currentRound;
    if (!r || r.result) return;
    var idx = -1;
    if (typeof idxOrType === "number") idx = idxOrType;
    else {
      for (var i = 0; i < state.powerups.length; i++) if (state.powerups[i].type === idxOrType) { idx = i; break; }
    }
    if (idx < 0 || idx >= state.powerups.length) return;
    var p = state.powerups[idx];
    state.powerups.splice(idx, 1);
    sfx.powerup_use();
    if (p.type === "hint") {
      // free hint
      var beforeScore = state.score;
      useHint();
      // refund the deducted points
      state.score = Math.max(state.score, beforeScore);
    } else if (p.type === "time") {
      r.timeRemaining = Math.min(r.timerInitial + 30, r.timeRemaining + 30);
      pushPopup("+30s", LOGICAL_W / 2, 320, "is-cyan");
    } else if (p.type === "showpath") {
      var prev = getLastCommitted();
      var dictSet = DICT_SETS[difficulty];
      var path = bfsNextNSteps(prev, r.puzzle.end, dictSet, 2);
      r.pathReveal = { nextWords: path, expiresAt: state.time + 6 };
      pushPopup("PATH SHOWN", LOGICAL_W / 2, 320, "is-tetris");
    } else if (p.type === "multx2") {
      state.x2RoundsLeft = 1;  // active for next puzzle
      pushPopup("x2 NEXT PUZZLE", LOGICAL_W / 2, 320, "is-tetris");
    } else if (p.type === "skip") {
      // Skip current puzzle without scoring or losing a life
      r.result = "skip";
      r.feedbackT = FEEDBACK_MS / 1000;
      pushPopup("SKIP (FREE)", LOGICAL_W / 2, CHAIN_TOP - 12, "is-bonus");
      pushPopup(r.puzzle.end, LOGICAL_W / 2, CHAIN_TOP + 90, "is-cyan");
    }
    renderPowerupTray();
    updateHud();
    updateRibbon();
  }
