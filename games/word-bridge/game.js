  function resizeCanvas() {
    var rect = canvas.getBoundingClientRect();
    dpr = Math.max(1, Math.min(2.5, window.devicePixelRatio || 1));
    canvas.width = Math.floor(rect.width * dpr);
    canvas.height = Math.floor(rect.height * dpr);
    var sx = rect.width / LOGICAL_W;
    var sy = rect.height / LOGICAL_H;
    scale = Math.min(sx, sy);
    offsetX = (rect.width - LOGICAL_W * scale) / 2;
    offsetY = (rect.height - LOGICAL_H * scale) / 2;
  }
  function drawBackground() {
    var w = canvas.width, h = canvas.height;
    var grad = ctx.createRadialGradient(w * 0.5, h * 0.4, 0, w * 0.5, h * 0.5, Math.max(w, h) * 0.7);
    grad.addColorStop(0, "#0e1a30");
    grad.addColorStop(0.6, "#08101e");
    grad.addColorStop(1, "#04060f");
    ctx.fillStyle = grad;
    ctx.fillRect(0, 0, w, h);
    if (state && state.bgStars) {
      ctx.save();
      ctx.translate(offsetX, offsetY);
      ctx.scale(scale, scale);
      for (var i = 0; i < state.bgStars.length; i++) {
        var s = state.bgStars[i];
        ctx.beginPath();
        ctx.arc(s.x, s.y, s.r, 0, Math.PI * 2);
        var tw = 0.55 + 0.45 * Math.sin(state.time * s.twinkleSpeed + s.twinkle);
        ctx.fillStyle = "rgba(245,196,81," + (0.18 * tw).toFixed(3) + ")";
        ctx.fill();
      }
      ctx.restore();
    }
  }
  function tileGeometry(n) {
    // Fit n letter slots in canvas width with margin
    var gap = 6;
    var maxW = LOGICAL_W - 80;
    var slotW = Math.floor((maxW - (n - 1) * gap) / n);
    slotW = Math.max(48, Math.min(80, slotW));
    return { w: slotW, h: Math.round(slotW * (76 / 64)), gap: gap };
  }
  function drawWordRow(word, y, opts) {
    opts = opts || {};
    var n = word.length;
    var geo = tileGeometry(n);
    var totalW = n * geo.w + (n - 1) * geo.gap;
    var startX = (LOGICAL_W - totalW) / 2;
    var gold = opts.gold;
    var cyan = opts.cyan;
    var emerald = opts.emerald;
    var label = opts.label;
    var dim = opts.dim;
    // Optional label
    if (label) {
      ctx.fillStyle = gold ? "rgba(245,196,81,0.85)" : (cyan ? "rgba(93,224,240,0.85)" : "rgba(208, 217, 244, 0.85)");
      ctx.font = "800 11px JetBrains Mono, monospace";
      ctx.textAlign = "center";
      ctx.fillText(label, LOGICAL_W / 2, y - 10);
    }
    for (var i = 0; i < n; i++) {
      var x = startX + i * (geo.w + geo.gap);
      var hi = (opts.activeSlot === i);
      var hint = (opts.hintIdx === i);
      var changed = (opts.changedSlots && opts.changedSlots[i]);
      drawSlot(x, y, geo.w, geo.h, word.charAt(i), {
        gold: gold,
        cyan: cyan,
        emerald: emerald,
        active: hi,
        hint: hint,
        hintTime: opts.hintTime,
        changed: changed,
        dim: dim
      });
    }
    return { startX: startX, w: geo.w, h: geo.h, gap: geo.gap };
  }
  function drawSlot(x, y, w, h, letter, opts) {
    opts = opts || {};
    // Drop shadow
    ctx.fillStyle = "rgba(0,0,0,0.45)";
    ctx.fillRect(x + 3, y + 4, w, h);
    // Body gradient
    var grad = ctx.createLinearGradient(x, y, x, y + h);
    if (opts.emerald) {
      grad.addColorStop(0, "#a8edcb");
      grad.addColorStop(0.5, "#5fcb96");
      grad.addColorStop(1, "#2c8b62");
    } else if (opts.cyan) {
      grad.addColorStop(0, "#bff5fb");
      grad.addColorStop(0.5, "#5de0f0");
      grad.addColorStop(1, "#2a8aa0");
    } else if (opts.gold) {
      grad.addColorStop(0, "#fbe09a");
      grad.addColorStop(0.5, "#e6b738");
      grad.addColorStop(1, "#a8821c");
    } else if (opts.changed) {
      grad.addColorStop(0, "#dee5fa");
      grad.addColorStop(0.5, "#a3b3e6");
      grad.addColorStop(1, "#6573a8");
    } else {
      grad.addColorStop(0, "#ecd9b1");
      grad.addColorStop(0.5, "#d2b986");
      grad.addColorStop(1, "#a8895c");
    }
    ctx.fillStyle = grad;
    ctx.fillRect(x, y, w, h);
    // Inner darker frame
    ctx.strokeStyle = "rgba(120, 84, 36, 0.55)";
    ctx.lineWidth = 2;
    ctx.strokeRect(x + 1, y + 1, w - 2, h - 2);
    // Top highlight
    var hi = ctx.createLinearGradient(x, y, x, y + 8);
    hi.addColorStop(0, "rgba(255,255,255,0.45)");
    hi.addColorStop(1, "rgba(255,255,255,0)");
    ctx.fillStyle = hi;
    ctx.fillRect(x + 1, y + 1, w - 2, 8);
    // Outer border
    var border = "#7a4f23";
    if (opts.gold) border = "#f5c451";
    if (opts.cyan) border = "#5de0f0";
    if (opts.emerald) border = "#4dd49b";
    if (opts.active) border = "#a991ff";
    ctx.strokeStyle = border;
    ctx.lineWidth = (opts.gold || opts.active || opts.hint) ? 2.4 : 1.5;
    ctx.strokeRect(x, y, w, h);
    if (opts.gold || opts.cyan || opts.active) {
      ctx.save();
      ctx.shadowColor = opts.cyan ? "rgba(93,224,240,0.7)" : (opts.active ? "rgba(169,145,255,0.8)" : "rgba(245,196,81,0.7)");
      ctx.shadowBlur = 14;
      ctx.strokeRect(x, y, w, h);
      ctx.restore();
    }
    // Hint highlight — steady glow (not frame-rate-dependent flash) using time-based sine
    if (opts.hint) {
      var hintAlpha = opts.hintTime != null
        ? (0.65 + 0.35 * Math.sin(opts.hintTime * 3.5))
        : 0.85;
      ctx.save();
      ctx.shadowColor = "rgba(245,196,81," + hintAlpha.toFixed(2) + ")";
      ctx.shadowBlur = 18;
      ctx.strokeStyle = "rgba(245,196,81," + hintAlpha.toFixed(2) + ")";
      ctx.lineWidth = 2.5;
      ctx.strokeRect(x, y, w, h);
      ctx.restore();
    }
    // Letter
    ctx.fillStyle = "#2a1c0a";
    ctx.textAlign = "center";
    ctx.textBaseline = "middle";
    var fontSize = Math.round(w * 0.62);
    ctx.font = "900 " + fontSize + "px Fraunces, serif";
    ctx.fillText(letter || "?", x + w / 2, y + h / 2 + 2);
    // Active slot caret
    if (opts.active) {
      ctx.strokeStyle = "#a991ff";
      ctx.lineWidth = 2;
      ctx.beginPath();
      ctx.moveTo(x + 4, y + h - 4);
      ctx.lineTo(x + w - 4, y + h - 4);
      ctx.stroke();
    }
  }
